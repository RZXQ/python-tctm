# 课堂练习3
# 使用键盘输入内容，并判断内容来自于哪一首诗
# 提示1：输入内容可以用input()接受并保存在变量中
# 提示2：使用成员运算符判断，内容在text1中输出t1；内容在text1中输出t2；不在这两首诗中，输出t3
text1 = '《出塞》,秦时明月汉时关，万里长征人未还。但使龙城飞将在，不教胡马度阴山。'
text2 = '《乌衣巷》,朱雀桥边野草花，乌衣巷口夕阳斜。旧时王谢堂前燕，飞入寻常百姓家。'
t1 = '出自唐朝王昌龄的《出塞》'
t2 = '出自唐朝刘禹锡的《乌衣巷》'
t3 = '未收录'
word = input('请输入词句')

if word in text1:
    print(t1)
elif word in text2:
    print(t2)
elif word not in text1 + text2:
    print(t3)
